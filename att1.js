// Declaração das variáveis
let altura = 5;
let largura = 15;

// Cálculo da área
let area = altura * largura;
console.log("Área:", area); // Exibe: Área: 75

// Cálculo do perímetro
let perimetro = 2 * (altura + largura);
console.log("Perímetro:", perimetro); // Exibe: Perímetro: 40

// Multiplicação
let multiplicacao = altura * largura;
console.log("Multiplicação:", multiplicacao); // Exibe: Multiplicação: 75

// Divisão
let divisao = largura / altura;
console.log("Divisão:", divisao); // Exibe: Divisão: 3

// Resto da divisão
let restoDivisao = largura % altura;
console.log("Resto da divisão:", restoDivisao); // Exibe: Resto da divisão: 0

// Cálculo da hipotenusa
let hipotenusa = Math.sqrt(altura**2 + largura**2);
console.log("Hipotenusa:", hipotenusa); // Exibe: Hipotenusa: 15.81 (aproximadamente)
